package ENTIDAD;
//en esta clase se usa para gestionar los usuarios del login
public class Usuarios {
    String Usuarios = "";
    String Nombre = "";
    public Usuarios(String Usuarios, String Nombre){
        this.Usuarios =Usuarios;
        this.Usuarios = Usuarios;
    }
    public String GetUsuario (){
        return Usuarios;
    }
    public String GetNombre(){
        return Nombre;
    }
}
